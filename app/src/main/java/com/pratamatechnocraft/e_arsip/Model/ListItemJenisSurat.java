package com.pratamatechnocraft.e_arsip.Model;

public class ListItemJenisSurat {
    private String idJenisSurat, jenisSurat;

    public ListItemJenisSurat(String idJenisSurat, String jenisSurat) {
        this.idJenisSurat = idJenisSurat;
        this.jenisSurat = jenisSurat;
    }

    public String getIdJenisSurat() {
        return idJenisSurat;
    }

    public String getJenisSurat() {
        return jenisSurat;
    }
}
