package com.pratamatechnocraft.e_arsip.Model;

public class ListItemBagianMendisposisikan {
    private String idBagian, namaBagian;

    public ListItemBagianMendisposisikan(String idBagian, String namaBagian) {
        this.idBagian = idBagian;
        this.namaBagian = namaBagian;
    }

    public String getIdBagian() {
        return idBagian;
    }

    public String getNamaBagian() {
        return namaBagian;
    }
}
